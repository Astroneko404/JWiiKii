class searchResult:
    title = ""
    url = ""
    content = ""

    def __init__(self, title, url, content):
        self.title = title
        self.url = url
        self.content = content

    def setTitle(self, t):
        self.title = t

    def setUrl(self, u):
        self.url = u

    def setContent(self, c):
        self.content = c



